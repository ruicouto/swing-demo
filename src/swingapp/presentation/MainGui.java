    /**
     * Handle event of list mouse click
     * @param evt 
     */
    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        //get the Game from the list
        Game g = (Game)jList1.getSelectedValue();
        //Update UI components
        jLabel2.setText(g.getPlatform());
        jLabel4.setText(g.getName());
        jSlider1.setValue(g.getRating());
        jCheckBox1.setSelected(g.isFinished());
        if(g.isHave()) { //is game owned?
            jRadioButton1.setSelected(true);
        } else { //set wanted
            jRadioButton2.setSelected(g.isWant());
        }
    }//GEN-LAST:event_jList1MouseClicked

