    /**
     * Update the games list
     */
    private void fillData() {
        DefaultListModel<Game> dlm = new DefaultListModel<Game>();
        for(Game g : games.getGames()) {
            dlm.addElement(g);
        }
        
        jList1.setModel(dlm);
    }

